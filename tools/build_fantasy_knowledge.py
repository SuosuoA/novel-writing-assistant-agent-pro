def main():
    """构建玄幻知识库"""
    workspace_root = Path(__file__).parent.parent
    knowledge_dir = workspace_root / "data" / "knowledge" / "fantasy"
    knowledge_dir.mkdir(parents=True, exist_ok=True)
    
    # 构建宗教知识库
    religion_knowledge = [
        create_knowledge_point(t, "xuanhuan", i+1)
        for i, t in enumerate(RELIGION_TEMPLATES)
    ]
    
    with open(knowledge_dir / "religion.json", 'w', encoding='utf-8') as f:
        json.dump(religion_knowledge, f, ensure_ascii=False, indent=2)
    print(f"[OK] Religion knowledge: {len(religion_knowledge)} points")
    
    # 构建神话知识库
    mythology_knowledge = [
        create_knowledge_point(t, "xuanhuan", i+1)
        for i, t in enumerate(MYTHOLOGY_TEMPLATES)
    ]
    
    with open(knowledge_dir / "mythology.json", 'w', encoding='utf-8') as f:
        json.dump(mythology_knowledge, f, ensure_ascii=False, indent=2)
    print(f"[OK] Mythology knowledge: {len(mythology_knowledge)} points")
    
    total = len(religion_knowledge) + len(mythology_knowledge)
    print(f"\n[DONE] Fantasy knowledge base built! Total: {total} points")
    print(f"[INFO] Location: {knowledge_dir}")


if __name__ == "__main__":
    main()
