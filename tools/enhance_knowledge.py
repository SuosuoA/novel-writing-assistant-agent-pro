def enhance_knowledge_file(json_path: Path, enhanced_items: list) -> int:
    """
    增强知识库文件内容
    
    Args:
        json_path: JSON文件路径
        enhanced_items: 增强后的知识点列表
        
    Returns:
        更新的知识点数量
    """
    try:
        with open(json_path, 'r', encoding='utf-8') as f:
            existing_data = json.load(f)
        
        # 创建ID到增强内容的映射
        enhanced_map = {item['knowledge_id']: item for item in enhanced_items}
        
        updated_count = 0
        for i, kp in enumerate(existing_data):
            kid = kp.get('knowledge_id')
            if kid in enhanced_map:
                # 更新知识点
                enhanced = enhanced_map[kid]
                existing_data[i].update({
                    'title': enhanced.get('title', kp['title']),
                    'content': enhanced.get('content', kp['content']),
                    'keywords': enhanced.get('keywords', kp.get('keywords', [])),
                    'difficulty': enhanced.get('difficulty', kp.get('difficulty', 'intermediate')),
                    'examples': enhanced.get('examples', []),
                    'common_mistakes': enhanced.get('common_mistakes', []),
                    'references': enhanced.get('references', []),
                    'updated_at': datetime.now().isoformat()
                })
                updated_count += 1
        
        # 保存更新
        with open(json_path, 'w', encoding='utf-8') as f:
            json.dump(existing_data, f, ensure_ascii=False, indent=2)
        
        return updated_count
    except Exception as e:
        print(f"更新失败 {json_path}: {e}")
        return 0


